package marsh.expr;

public class Expressions {

    public static void main (String[] args) {
        int x; // Declaring a new integer variable, x

        x = 5; // assigning a value to x

        System.out.println("x = " + x);  // Print the value of x, concatenating a string and an int

        int y = ++x; // Try this with x++ instead! What changes?

        System.out.println("x = " + x + " y = " + y); // print results of expression above


    }

}
